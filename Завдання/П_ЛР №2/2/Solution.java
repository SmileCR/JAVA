/*
Вивести на екран числа від 5 до 1 (використовуючи оператор циклу).
 */
// Клас Solution
public class Solution {
    public static void main(String[] args) {
        // Цикл для виведення чисел від 5 до 1
        for (int i = 5; i >= 1; i--) {
            // Виведення поточного значення з пробілом
            System.out.print(i + " ");
        }
    }
}
