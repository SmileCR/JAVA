/*
Вивести на екран числа від 1 до 5 (використовуючи оператор циклу).
 */
// Клас Solution
public class Solution {
    public static void main(String[] args) {
        // Цикл для виведення чисел від 1 до 5
        for (int i = 1; i <= 5; i++) {
            // Виведення поточного значення з пробілом
            System.out.print(i + " ");
        }
    }
}
